public class Triangle {
    double angle1;
    double angle2;
    double angle3;

    Triangle(double angle1, double angle2, double angle3) {
        this.angle1 = angle1;
        this.angle2 = angle2;
        this.angle3 = angle3;
    }

    double getAngle1() {
        return angle1;
    }

    double getAngle2() {
        return angle2;
    }

    double getAngle3() {
        return angle3;
    }
}
